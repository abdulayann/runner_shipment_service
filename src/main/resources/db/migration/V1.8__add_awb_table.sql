CREATE TABLE IF NOT EXISTS awb (
    id bigint NOT NULL GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    guid uuid NOT NULL DEFAULT uuid_generate_v4(),
    shipment_id BIGINT NOT NULL ,
    awb_shipment_info jsonb,
    awb_notify_party_info jsonb,
    awb_routing_info jsonb,
    awb_cargo_info jsonb,
    awb_payment_info jsonb,
    awb_other_charges_info jsonb,
    awb_other_info jsonb,
    awb_oci_info jsonb,
    awb_goods_description_info jsonb,
    awb_packing_info jsonb,
    awb_special_handling_codes_Mappings jsonb,
    tenant_id integer,
    created_at timestamp  NOT NULL,
    created_by integer,
    updated_at timestamp  NOT NULL,
    updated_by integer,
    is_deleted BOOLEAN DEFAULT FALSE
);