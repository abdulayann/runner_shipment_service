CREATE TABLE sailing_schedule (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    shipment_id bigint NOT NULL,
    scac VARCHAR(255),
    carrier_name VARCHAR(255),
    service_name VARCHAR(255),
    vessel_name VARCHAR(255),
    vessel_display_name VARCHAR(255),
    vessel_mmsi VARCHAR(255),
    voyage_number VARCHAR(255),
    imo_number VARCHAR(255),
    origin_unloc_reference_guid VARCHAR(255),
    origin_unloc VARCHAR(255),
    origin_country VARCHAR(255),
    origin_city_name VARCHAR(255),
    origin_subdivision VARCHAR(255),
    origin_terminal VARCHAR(255),
    destination_unloc VARCHAR(255),
    destination_unloc_reference_guid VARCHAR(255),
    destination_country VARCHAR(255),
    destination_subdivision VARCHAR(255),
    destination_city_name VARCHAR(255),
    destination_terminal VARCHAR(255),
    origin_estimated_departure_date TIMESTAMP,
    origin_actual_departure_date TIMESTAMP,
    destination_estimated_arrival_date TIMESTAMP,
    destination_actual_arrival_date TIMESTAMP,
    estimated_terminal_cutoff TIMESTAMP,
    terminal_cutoff TIMESTAMP,
    booking_cutoff TIMESTAMP,
    ship_instruction_cutoff TIMESTAMP,
    hazardous_booking_cutoff TIMESTAMP,
    verified_gross_mass_cutoff TIMESTAMP,
    reefer_cutoff TIMESTAMP,
    latest_full_equipment_delivery TIMESTAMP,
    earliest_full_equipment_dropoff_date TIMESTAMP,
    earliest_empty_equipment_pickup_date TIMESTAMP,
    total_duration INTEGER,
    schedule_type VARCHAR(255),
    insert_date TIMESTAMP,
    insert_user_id VARCHAR(255),
    update_date TIMESTAMP,
    update_user_id VARCHAR(255),
    source VARCHAR(255),
    vessel_id VARCHAR(255),
    vessel_guid uuid,
    sailing_schedule_legs JSONB,
    guid uuid,
    created_at TIMESTAMP,
    updated_at TIMESTAMP,
    created_by VARCHAR,
    updated_by VARCHAR,
    is_deleted BOOLEAN DEFAULT FALSE
);
